    private void displayResults(String studentId) {
    // Assuming a simple schema where student names are in a separate table
    // Adjust this query to fetch the student name alongside their marks
    String query = "SELECT m.module_name, m.percentage, m.grade, s.firstname, s.lastname " +
                   "FROM Marks m " +
                   "INNER JOIN Student s ON m.student_id = s.id " +
                   "WHERE m.student_id = ?";

    try {
        Conn c = new Conn();
        PreparedStatement ps = c.getConnection().prepareStatement(query);
        
        // Set the student ID for the query
        ps.setInt(1, Integer.parseInt(studentId));
        
        ResultSet rs = ps.executeQuery();
        
        DefaultTableModel model = (DefaultTableModel) tableMarks.getModel();
        model.setRowCount(0); // Clear existing data
        
        String studentName = ""; // Variable to hold the student's name

        while (rs.next()) {
            if (studentName.isEmpty()) { // Set the student's name from the first row
                studentName = rs.getString("firstname") + " " + rs.getString("lastname");
                Namehere.setText(studentName); // Set the student's name to the label
            }

            String moduleName = rs.getString("module_name");
            int percentage = rs.getInt("percentage");
            String grade = rs.getString("grade");
            
            Object[] row = { moduleName, percentage, grade };
            model.addRow(row);
        }

        if (studentName.isEmpty()) { // If no records found
            Namehere.setText("No records found");
        }
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error in fetching data from database: " + e.getMessage());
        e.printStackTrace();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Invalid Student ID format.");
    }
}

    
