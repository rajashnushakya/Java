 private boolean isValidPhoneNumber(String phoneNumber) {
    // regex pattern for a 10-digit phone number
    String phoneRegex = "^\\d{10}$";

    // Compile the regex pattern
    Pattern pattern = Pattern.compile(phoneRegex);

    // Check if the phoneNumber is null
    if (phoneNumber == null) {
        return false;
    }

    // Match the phoneNumber against the regex pattern
    return pattern.matcher(phoneNumber).matches();
}

    private boolean isValidEmail(String email) {
    // regex pattern for a small lettere and number of  email
    String emailRegex = "^[a-z]+[0-9]+@gmail\\.com$";
    Pattern pattern = Pattern.compile(emailRegex);
    if (email == null) {
        return false;
    }
    return pattern.matcher(email).matches();
}

private boolean isValidPassword(String password) {
    // regex pattern for the password which must contain one Uppercase and number
    String passwordRegex = "^(?=.*[A-Z])(?=.*\\d).+$";


    // Compile the regex pattern
    Pattern pattern = Pattern.compile(passwordRegex);

    // Check if the password is null
    if (password == null) {
        return false;
    }

    // Match the password against the regex pattern
    return pattern.matcher(password).matches();
}


    private void btnCreateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCreateMouseClicked
                                      
    if(evt.getSource() == btnCreate){
        try {
            String firstName = fnameField.getText();
            String lastName = lnameField.getText();
            String email = emailField.getText();           
            String password = psField.getText();
            String address = addrField.getText();
            String phoneNumber = phnoField.getText();
            String course = cmbCourse.getSelectedItem().toString(); 
            String role = cmbRole.getSelectedItem().toString();
            Date date = jDateChooser1.getDate();
            java.sql.Date sqlDate = null;
            
            // Validation as per something goes wrong 
            if (date != null) {
                sqlDate = new java.sql.Date(date.getTime());
            }
            
             if (!isValidEmail(email)) {
            JOptionPane.showMessageDialog(null, "Please enter a valid email address.");
            return; 
            }
              if (!isValidPhoneNumber(phoneNumber)) {
            JOptionPane.showMessageDialog(null, "Please enter a valid phone number.");
            return; 
            }
             if (!isValidPassword(password)) {
            JOptionPane.showMessageDialog(null, "Please enter a valid password.");
            return; 
            }
            String query = ""; 
            String selectedCourse = cmbCourse.getSelectedItem().toString();
            
            // Check the role and prepare the query accordingly
            if ("Student".equals(role)) {
                query = "INSERT INTO student ( Email, Address, PhoneNumber, Password, DateOfBirth, firstname, lastname, course) VALUES ( ?, ?, ?, ?, ?, ?, ?, ?)";
            } else if ("Tutor".equals(role)) {
                query = "INSERT INTO tutor ( Email, Address, PhoneNumber, Password, DateOfBirth, firstname, lastname, course) VALUES ( ?, ?, ?, ?, ?, ?, ?, ?)";
            } else if ("Admin".equals(role)) {
                query = "INSERT INTO admin ( Email, Address, PhoneNumber, Password, DateOfBirth, firstname, lastname) VALUES ( ?, ?, ?, ?, ?, ?, ?)";
            } else {
                // Handling unexpected role
                JOptionPane.showMessageDialog(null, "Invalid role selected");
                return;
            }

            Conn c = new Conn();
            try(PreparedStatement ps = c.getConnection().prepareStatement(query)) {
                ps.setString(1, email);
                ps.setString(2, address);
                ps.setString(3, phoneNumber);
                ps.setString(4, password);
                ps.setDate(5, sqlDate);
                ps.setString(6, firstName);
                ps.setString(7, lastName);
                // if the role is not admin show and insert the course comboBox
                if ("Student".equals(role) || "Tutor".equals(role)){
                    ps.setString(8, course);
                }
                       
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Registered Successfully");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter valid numbers for ID and Phone Number");
            e.printStackTrace();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Registration Failed");
            e.printStackTrace();
        }
    }


    }//GEN-LAST:event_btnCreateMouseClicked

