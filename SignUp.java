private void populateCoursesComboBox() {
    String query = "SELECT name FROM course"; 
    try {
        Conn c = new Conn();
        Connection conn = c.getConnection(); // Get the Connection object from your Conn class
        try (Statement stmt = conn.createStatement()) { 
            ResultSet rs = stmt.executeQuery(query); 
            cmbCourse.removeAllItems(); // Clear existing items
            
            while (rs.next()) {
                String courseName = rs.getString("name"); 
                cmbCourse.addItem(courseName); // Add course name to the combo box
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Failed to populate courses");
        e.printStackTrace();
    }
}

