private void fetchDataAndShowInTable() {
    String query = "SELECT id, firstname, lastname, course FROM student"; // Adjust table name and columns as necessary
    try {
        Conn c = new Conn();
        java.sql.Connection conn = c.getConnection();
        java.sql.Statement stmt = conn.createStatement();
        var rs = stmt.executeQuery(query);

        DefaultTableModel model = (DefaultTableModel) tablestudent.getModel();
        model.setRowCount(0); // Clear existing data

        while (rs.next()) {
            int id = rs.getInt("id");
            String fname = rs.getString("firstname"); // Use column name as in the database
            String lname = rs.getString("lastname"); // Use column name as in the database
            String course = rs.getString("course"); // Use column name as in the database
            Object[] row = { id, fname + " " + lname, course }; // Assuming you want to combine first name and last name
            model.addRow(row); // Add row to the model
        }
        tablestudent.setModel(model); // This line may be redundant if jTable1 already uses this model
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error in fetching data from database");
        e.printStackTrace();
    }
}

       
